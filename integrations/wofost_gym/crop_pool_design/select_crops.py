def _resolve_traits_dir(traits_dir: Path, trait_schema: str) -> Path:
    """Resolve root crop_traits dir to a schema-specific directory when needed."""
    if traits_dir.name == trait_schema:
        return traits_dir
    if trait_schema == DEFAULT_TRAIT_SCHEMA:
        schema_dir = traits_dir / trait_schema
        return schema_dir if schema_dir.exists() else traits_dir
    return traits_dir / trait_schema


def load_crop_traits(traits_dir: Path, trait_schema: str) -> Dict[str, Dict[str, float]]:
    """Load crop trait JSON cards, returning {crop_name: {feature: value}}."""
    crops: Dict[str, Dict[str, float]] = {}
    resolved_traits_dir = _resolve_traits_dir(traits_dir, trait_schema)
    feature_names = SCHEMA_FEATURE_NAMES[trait_schema]

    for f in sorted(resolved_traits_dir.glob("*.json")):
        with open(f) as fh:
            card = json.load(fh)
        crop = card["crop"]
        if crop in EXCLUDED_CROPS:
            continue
        if trait_schema == "traits_v1_23d":
            core = card["core_facts"]
            fotb = core["assimilation_and_partition"]["FOTB_storage_onset_DVS"]
            if fotb is None:
                continue
            crops[crop] = {
                "TBASEM": core["temperature"]["TBASEM_C"],
                "TSUM_total": core["phenology"]["TSUM_total_Cd"],
                "IDSL": core["phenology"]["IDSL"],
                "RDMCR": core["root_and_water"]["RDMCR_cm"],
                "DEPNR": core["root_and_water"]["DEPNR"],
                "AMAX": core["assimilation_and_partition"]["AMAX_peak"],
                "FOTB_onset": fotb,
                "IAIRDU": core["root_and_water"]["IAIRDU"],
            }
            continue

        if trait_schema == "traits_v1_6d":
            source_facts = card.get("source_facts", {})
            fotb = source_facts.get("season_urgency", {}).get("FOTB_storage_onset_DVS")
            if fotb is None:
                continue
            decision_axes = card["core_facts"]["decision_axes"]
            crops[crop] = {name: float(decision_axes[name]) for name in feature_names}
            continue

        raise ValueError(f"Unsupported trait schema: {trait_schema}")
    return crops


def build_distance_matrix(
    crop_names: List[str],
    crops: Dict[str, Dict[str, float]],
    feature_names: List[str],
) -> Tuple[np.ndarray, np.ndarray]:
    """Min-max normalise traits and compute pairwise Euclidean distances."""
    raw = np.array(
        [[crops[c][f] for f in feature_names] for c in crop_names],
        dtype=np.float64,
    )
    mins = raw.min(axis=0)
    maxs = raw.max(axis=0)
    scales = maxs - mins
    scales[scales == 0] = 1.0
    normed = (raw - mins) / scales

    n = len(crop_names)
    dist = np.zeros((n, n))
    for i in range(n):
        for j in range(n):
            dist[i, j] = np.linalg.norm(normed[i] - normed[j])
    return normed, dist


def _volume_proxy(normed: np.ndarray, idxs: List[int]) -> float:
    """Product of per-feature ranges (proxy for hyper-rectangle volume)."""
    sub = normed[idxs]
    fr = sub.max(axis=0) - sub.min(axis=0)
    return float(np.prod(fr[fr > 0]))


def _min_pairwise(dist: np.ndarray, idxs: List[int]) -> float:
    """Minimum pairwise distance within a set of indices."""
    pairs = list(combinations(idxs, 2))
    if not pairs:
        return 0.0
    return min(dist[i, j] for i, j in pairs)


def select_fixed_ood(
    crop_names: List[str],
    normed: np.ndarray,
    dist: np.ndarray,
    n_ood: int = 4,
) -> Tuple[List[str], List[str]]:
    """Select the best fixed OOD set of size n_ood.

    Optimises a weighted combination of:
      - ID pool trait-space volume (higher = better; ID pool retains coverage)
      - OOD set spread (higher = better; OOD tests diverse directions)
    """
    n = len(crop_names)
    best_combo = None
    best_score = -1.0

    for ood_combo in combinations(range(n), n_ood):
        id_pool = [i for i in range(n) if i not in ood_combo]
        id_vol = _volume_proxy(normed, id_pool)
        ood_spread = _min_pairwise(dist, list(ood_combo))
        score = id_vol * 0.7 + ood_spread * 0.3
        if score > best_score:
            best_score = score
            best_combo = ood_combo

    ood_names = [crop_names[i] for i in best_combo]
    id_pool = [c for c in crop_names if c not in ood_names]
    return ood_names, id_pool


def select_best_k(
    pool_names: List[str],
    crop_names: List[str],
    dist: np.ndarray,
    k: int,
) -> List[str]:
    """Exhaustive search for the k-crop subset maximising min pairwise dist."""
    idx_map = {c: i for i, c in enumerate(crop_names)}
    pool_idxs = [idx_map[c] for c in pool_names]

    best_combo = None
    best_score = -1.0
    for combo in combinations(pool_idxs, k):
        md = _min_pairwise(dist, list(combo))
        if md > best_score:
            best_score = md
            best_combo = combo
    return [crop_names[i] for i in best_combo]


def greedy_maxmin_expand(
    seed_names: List[str],
    target_size: int,
    pool_names: List[str],
    crop_names: List[str],
    dist: np.ndarray,
) -> List[str]:
    """Greedily expand seed set within pool by max-min distance."""
    idx_map = {c: i for i, c in enumerate(crop_names)}
    pool_idxs = set(idx_map[c] for c in pool_names)
    selected = [idx_map[c] for c in seed_names]
    remaining = [i for i in pool_idxs if i not in selected]
    order = list(seed_names)

    while len(selected) < target_size and remaining:
        best_idx = max(remaining, key=lambda r: min(dist[r, s] for s in selected))
        selected.append(best_idx)
        remaining.remove(best_idx)
        order.append(crop_names[best_idx])
    return order


def coverage_metrics(
    selected: List[str],
    crop_names: List[str],
    normed: np.ndarray,
    dist: np.ndarray,
    feature_names: List[str],
) -> Dict[str, Any]:
    """Compute trait-space coverage statistics for a crop subset."""
    idx_map = {c: i for i, c in enumerate(crop_names)}
    idxs = [idx_map[c] for c in selected]
    sub = normed[idxs]

    feat_ranges = sub.max(axis=0) - sub.min(axis=0)
    pairs = list(combinations(idxs, 2))
    pair_dists = [dist[i, j] for i, j in pairs]

    min_d = np.min(pair_dists)
    min_pair = None
    for i, j in pairs:
        if dist[i, j] == min_d:
            min_pair = (crop_names[i], crop_names[j])
            break

    vol = float(np.prod(feat_ranges[feat_ranges > 0]))
    return {
        "avg_pairwise_dist": float(np.mean(pair_dists)),
        "min_pairwise_dist": float(min_d),
        "min_pair": min_pair,
        "volume_proxy": vol,
        "per_feature_range": {f: float(r) for f, r in zip(feature_names, feat_ranges)},
    }


def ood_distances(
    id_crops: List[str],
    ood_crops: List[str],
    crop_names: List[str],
    dist: np.ndarray,
) -> List[Dict[str, Any]]:
    """For each OOD crop, find its nearest ID crop and distance."""
    idx_map = {c: i for i, c in enumerate(crop_names)}
    result = []
    for oc in ood_crops:
        oi = idx_map[oc]
        nearest = min(id_crops, key=lambda c: dist[oi, idx_map[c]])
        d = dist[oi, idx_map[nearest]]
        result.append({"crop": oc, "nearest_id": nearest, "distance": float(d)})
    result.sort(key=lambda x: -x["distance"])
    return result


