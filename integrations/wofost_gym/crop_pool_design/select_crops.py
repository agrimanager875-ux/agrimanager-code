#!/usr/bin/env python3
"""Crop selection for multi-crop RL experiments.

This script is the canonical, reproducible source-of-truth for which crops
are used as ID (in-distribution) training crops and which are held out as
a fixed OOD (out-of-distribution) evaluation set.

Experimental design: Crop Diversity Scaling
-------------------------------------------
Core question: under a fixed training budget (3200 episodes), does
increasing trait diversity (more crops, fewer samples per crop) improve
OOD generalization?

    Condition   ID crops   samples/crop
    ─────────   ────────   ────────────
    4-ID        4          800
    8-ID        8          400
    16-ID       16         200

All three conditions share the **same fixed OOD eval set** (4 crops) so
that OOD performance is directly comparable across conditions.

Selection methodology
---------------------
1. Load structured crop-trait JSON cards (generated by build_crop_traits.py).
2. Exclude crops that cannot complete a 240-day episode (verified by
   check_crop_maturity.py across 12 location × year weather scenarios).
3. Normalise the selected trait vector for each crop.
4. **Select the OOD set first**: exhaustive search over C(20,4)=4845
   candidate OOD sets, scoring each by a weighted combination of
   (a) remaining ID pool trait-space volume and (b) OOD set spread.
5. From the remaining 16-crop ID pool:
   - 4-ID: exhaustive search for the 4-crop subset that maximises
     the minimum pairwise distance.
   - 8-ID: greedily expand the optimal 4 by adding the crop farthest
     from the current set (max-min diversification).
   - 16-ID: use the full ID pool.
6. Report coverage metrics and OOD distances.

Trait schemas
-------------
traits_v1_23d selection subspace (8 dimensions)
TBASEM        base temperature for emergence (°C)
TSUM_total    total thermal-time requirement (°C·d)
IDSL          development driver: temp-only(0) / +daylength(1) / +vernalization(2)
RDMCR         maximum rooting depth (cm)
DEPNR         drought tolerance proxy (higher = more tolerant)
AMAX          peak leaf CO₂ assimilation rate (kg ha⁻¹ hr⁻¹)
FOTB_onset    DVS at which storage-organ allocation begins
IAIRDU        air ducts in roots (0 or 1)

traits_v1_6d selection subspace (6 dimensions)
season_urgency
development_constraint
water_priority
nitrogen_priority
phosphorus_priority
potassium_priority

Usage
-----
    # Print selections and coverage analysis
    python integrations/wofost_gym/crop_pool_design/select_crops.py

    # Write one weather-pool YAML config per usable crop
    python integrations/wofost_gym/crop_pool_design/select_crops.py --write-per-crop-configs
"""

