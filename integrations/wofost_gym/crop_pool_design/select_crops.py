def main() -> None:
    parser = argparse.ArgumentParser(
        description="Select ID/OOD crop splits by maximising trait-space coverage.",
    )
    parser.add_argument(
        "--traits-dir", type=str, default=None,
        help="Path to crop trait JSON directory",
    )
    parser.add_argument(
        "--trait-schema",
        type=str,
        default=DEFAULT_TRAIT_SCHEMA,
        choices=sorted(SCHEMA_FEATURE_NAMES.keys()),
        help="Trait schema to use for split selection.",
    )
    parser.add_argument(
        "--write-per-crop-configs", action="store_true",
        help=(
            "Write one weather-pool YAML config per usable crop to "
            "integrations/wofost_gym/dataset_tools/weather_pool_configs/."
        ),
    )
    args = parser.parse_args()

    traits_dir = Path(args.traits_dir) if args.traits_dir else DEFAULT_TRAITS_DIR
    trait_schema = args.trait_schema
    feature_names = SCHEMA_FEATURE_NAMES[trait_schema]
    resolved_traits_dir = _resolve_traits_dir(traits_dir, trait_schema)

    # ── Load and validate ──────────────────────────────────────────────────
    crops = load_crop_traits(traits_dir, trait_schema)
    crop_names = sorted(crops.keys())
    print(f"Usable crops: {len(crop_names)}")
    print(f"Trait schema: {trait_schema} ({SCHEMA_LABELS[trait_schema]})")
    print(f"Traits dir: {resolved_traits_dir}")
    print(f"Excluded: {list(EXCLUDED_CROPS.keys())}")
    for crop, reason in EXCLUDED_CROPS.items():
        print(f"  {crop}: {reason}")

    # ── Build distance matrix ──────────────────────────────────────────────
    normed, dist = build_distance_matrix(crop_names, crops, feature_names)

    # ── Step 1: Select fixed OOD set ───────────────────────────────────────
    print(f"\n{'='*105}")
    print("Step 1: Select fixed OOD evaluation set (4 crops)")
    print(f"{'='*105}")
    ood_names, id_pool = select_fixed_ood(crop_names, normed, dist, n_ood=4)
    print(f"  OOD set:  {ood_names}")
    print(f"  ID pool:  {id_pool} ({len(id_pool)} crops)")

    # ── Step 2: Select optimal ID sets from pool ───────────────────────────
    print(f"\n{'='*105}")
    print("Step 2: Select optimal ID sets from the 16-crop pool")
    print(f"{'='*105}")

    # 4-ID: exhaustive search
    id_4 = select_best_k(id_pool, crop_names, dist, k=4)
    # 8-ID: greedy expansion from optimal 4
    id_8 = greedy_maxmin_expand(id_4, 8, id_pool, crop_names, dist)
    # 16-ID: full pool
    id_16 = list(id_pool)

    # ── Metrics ────────────────────────────────────────────────────────────
    m4 = coverage_metrics(id_4, crop_names, normed, dist, feature_names)
    m8 = coverage_metrics(id_8, crop_names, normed, dist, feature_names)
    m16 = coverage_metrics(id_16, crop_names, normed, dist, feature_names)

