def _bucket_level(value: float) -> str:
    if value < 0.34:
        return "low"
    if value < 0.67:
        return "medium"
    return "high"


def _trait_notes_23d(d: Dict[str, float]) -> str:
    notes = []
    if d["TBASEM"] <= 3:    notes.append("cool")
    elif d["TBASEM"] <= 8:  notes.append("temperate")
    else:                   notes.append("warm")
    if d["TSUM_total"] >= 2300:   notes.append("long-season")
    elif d["TSUM_total"] >= 1700: notes.append("medium")
    else:                         notes.append("short")
    if d["RDMCR"] >= 150:  notes.append("deep-root")
    elif d["RDMCR"] < 80:  notes.append("shallow-root")
    if d["DEPNR"] <= 3.0:  notes.append("drought-sensitive")
    elif d["DEPNR"] >= 5.0: notes.append("very-tolerant")
    if d["AMAX"] >= 70:    notes.append("high-AMAX")
    elif d["AMAX"] <= 30:  notes.append("low-AMAX")
    if d["IDSL"] == 0:     notes.append("temp-only")
    elif d["IDSL"] == 2:   notes.append("vernalization")
    if d["IAIRDU"] == 1:   notes.append("air-ducts")
    if d["FOTB_onset"] <= 0.95:  notes.append("early-storage")
    elif d["FOTB_onset"] >= 1.4: notes.append("very-late-storage")
    return ", ".join(notes)


def _trait_notes_6d(d: Dict[str, float]) -> str:
    return ", ".join([
        f"season={_bucket_level(d['season_urgency'])}",
        f"dev={_bucket_level(d['development_constraint'])}",
        f"water={_bucket_level(d['water_priority'])}",
        f"N={_bucket_level(d['nitrogen_priority'])}",
        f"P={_bucket_level(d['phosphorus_priority'])}",
        f"K={_bucket_level(d['potassium_priority'])}",
    ])


def print_condition(
    label: str,
    id_crops: List[str],
    ood_crops: List[str],
    crops: Dict[str, Dict[str, float]],
    metrics: Dict[str, Any],
    ood_info: List[Dict[str, Any]],
    samples_per_crop: int,
    feature_names: List[str],
    trait_schema: str,
) -> None:
    print(f"\n{'─'*105}")
    print(f"  {label}  ({samples_per_crop} samples/crop, {len(id_crops)*samples_per_crop} total)")
    print(f"{'─'*105}")

    if trait_schema == "traits_v1_23d":
        print(f"  {'Crop':<14} {'TBASE':>5} {'TSUMt':>6} {'IDSL':>4} "
              f"{'RDMCR':>5} {'DEPNR':>5} {'AMAX':>5} {'FOTB':>5} {'AIR':>3}   Key direction")
        print(f"  {'-'*100}")
        for c in id_crops:
            d = crops[c]
            print(f"  {c:<14} {d['TBASEM']:>5.0f} {d['TSUM_total']:>6.0f} "
                  f"{d['IDSL']:>4.0f} {d['RDMCR']:>5.0f} {d['DEPNR']:>5.1f} "
                  f"{d['AMAX']:>5.0f} {d['FOTB_onset']:>5.2f} {d['IAIRDU']:>3.0f}   "
                  f"{_trait_notes_23d(d)}")
    else:
        header = f"  {'Crop':<14} {'Season':>6} {'Dev':>6} {'Water':>6} {'N':>6} {'P':>6} {'K':>6}   Key direction"
        print(header)
        print(f"  {'-' * (len(header) - 2)}")
        for c in id_crops:
            d = crops[c]
            print(
                f"  {c:<14} "
                f"{d['season_urgency']:>6.2f} "
                f"{d['development_constraint']:>6.2f} "
                f"{d['water_priority']:>6.2f} "
                f"{d['nitrogen_priority']:>6.2f} "
                f"{d['phosphorus_priority']:>6.2f} "
                f"{d['potassium_priority']:>6.2f}   "
                f"{_trait_notes_6d(d)}"
            )

    print(f"\n  Coverage: vol={metrics['volume_proxy']:.4f}  "
          f"avg_dist={metrics['avg_pairwise_dist']:.3f}  "
          f"min_dist={metrics['min_pairwise_dist']:.3f} "
          f"({metrics['min_pair'][0]}-{metrics['min_pair'][1]})")

    avg_ood = np.mean([o["distance"] for o in ood_info])
    print(f"  OOD avg distance: {avg_ood:.3f}")
    for o in ood_info:
        print(f"    {o['crop']:<14} -> {o['nearest_id']:<14} dist={o['distance']:.3f}")


