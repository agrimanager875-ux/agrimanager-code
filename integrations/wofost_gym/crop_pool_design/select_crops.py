def write_per_crop_weather_config(
    crop: str,
    num_train: int = 3200,
    num_val: int = 128,
    num_test: int = 512,
) -> Path:
    """Write a single-crop weather pool YAML config."""
    config = {
        "env_name": "wofost_gym",
        "env_id": "lnpkw-v0",
        "wofost_gym_path": "../AgriManagerExternal/WOFOSTGym",
        "save_folder": f"/tmp/wofost_configs_pool_crop_{crop}/",
        "data_mode": "multi_crop_ood",
        "train_crops": [crop],
        "eval_crops": [crop],
        "num_train_samples": num_train,
        "num_val_samples": num_val,
        "num_test_samples": num_test,
        "generation_seed": 42,
        "year_range": [1984, 2019],
        "min_dvs_threshold": 1.5,
        "crop_ranges": {crop: CROP_RANGES[crop]},
    }

    out_path = POOL_CONFIG_DIR / f"pool_crop_{crop}.yaml"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, "w") as f:
        yaml.dump(config, f, default_flow_style=False, sort_keys=False)
    return out_path


